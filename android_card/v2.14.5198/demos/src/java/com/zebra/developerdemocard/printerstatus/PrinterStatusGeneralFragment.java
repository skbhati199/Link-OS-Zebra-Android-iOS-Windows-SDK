package com.zebra.developerdemocard.printerstatus;

import android.os.Bundle;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ListView;

import com.zebra.developerdemocard.R;

public class PrinterStatusGeneralFragment extends Fragment {

    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        View rootView = inflater.inflate(R.layout.fragment_settings_list, container, false);

        ListView listView = rootView.findViewById(R.id.listView);
        listView.setAdapter(((PrinterStatusDemoActivity) getActivity()).getPrinterStatusGeneralAdapter());
        listView.setEmptyView(rootView.findViewById(R.id.settingsListEmptyView));

        return rootView;
    }
}
